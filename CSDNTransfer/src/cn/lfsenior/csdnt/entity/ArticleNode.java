package cn.lfsenior.csdnt.entity;

public class ArticleNode {
	private String maxpagenum;
	private String articleuri;
	private String tilte;
	private String labels;
	private String date;
	private String vist;
	private String article;
	public String getMaxpagenum() {
		return maxpagenum;
	}
	public void setMaxpagenum(String maxpagenum) {
		this.maxpagenum = maxpagenum;
	}
	public String getArticleuri() {
		return articleuri;
	}
	public void setArticleuri(String articleuri) {
		this.articleuri = articleuri;
	}
	public String getTilte() {
		return tilte;
	}
	public void setTilte(String tilte) {
		this.tilte = tilte;
	}
	public String getLabels() {
		return labels;
	}
	public void setLabels(String labels) {
		this.labels = labels;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getVist() {
		return vist;
	}
	public void setVist(String vist) {
		this.vist = vist;
	}
	public String getArticle() {
		return article;
	}
	public void setArticle(String article) {
		this.article = article;
	}
}
